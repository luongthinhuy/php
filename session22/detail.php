	<p>Phương Khánh đoạt danh hiệu Á hậu 2 cuộc thi Hoa Hậu Biển Việt Nam toàn cầu hồi tháng 4. Đến tháng 10, cô được cử đi thi Miss Earth 2018 nhờ tham gia vòng casting miền Bắc của Ngôi sao danh vọng - dự án tìm kiếm đại diện Việt Nam tại các cuộc thi hoa hậu, người mẫu quốc tế - ở Hà Nội.Phương Khánh đang là sinh viên ngành marketing của Đại học Curtin Singapore. Người đẹp Việt Nam là cái tên không nổi bật, ít nhận được sự quan tâm. </p>
	<p> Cô mặc đồ bơi hai mảnh, gương mặt rạng rỡ.Ở thử thách không trang điểm và catwalk trước giám khảo vào ngày 13/10, Phương Khánh tiếp tục được đánh giá cao bởi gương mặt sáng, tươi tắn.</p>
	<p>Tối 3/11, Phương Khánh gây bất ngờ khi chiến thắng tại Miss Earth, trở thành người đẹp đầu tiên của Việt Nam đăng quang Hoa hậu Trái đất. Đây cũng là thành tích cao nhất của Việt Nam ở một trong bốn đấu trường nhan sắc quốc tế lớn (Miss Universe, Miss World, Miss International và Miss Earth).</p>
